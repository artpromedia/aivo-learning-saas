export async function seedTranslations(db: Database) {
  // Seed locales
  for (const locale of SEED_LOCALES) {
    await db
      .insert(locales)
      .values(locale)
      .onConflictDoNothing();
  }

  // Seed namespaces
  const nsIds = new Map<string, string>();
  for (const ns of SEED_NAMESPACES) {
    const [row] = await db
      .insert(translationNamespaces)
      .values(ns)
      .onConflictDoNothing()
      .returning();

    if (row) {
      nsIds.set(ns.key, row.id);
    } else {
      const existing = await db
        .select()
        .from(translationNamespaces)
        .where(eq(translationNamespaces.key, ns.key))
        .limit(1);
      if (existing.length) {
        nsIds.set(ns.key, existing[0].id);
      }
    }
  }

  // Seed English translations
  for (const [nsKey, keys] of Object.entries(SEED_TRANSLATIONS)) {
    const nsId = nsIds.get(nsKey);
    if (!nsId) continue;

    const values = Object.entries(keys).map(([key, value]) => ({
      namespaceId: nsId,
      localeCode: "en",
      key,
      value,
      isVerified: true,
      updatedAt: new Date(),
    }));

    if (values.length > 0) {
      await db
        .insert(translations)
        .values(values)
        .onConflictDoNothing();
    }
  }
}
